                method: "POST",
                start_time: new Date().getTime(),
                data: {
                    text: $('#text-verify-ecc').val(),
                    signature: $('#text-signature-ecc').val(),
                },
                success: function(data) {
                    console.log(data);
                    if (!data.verify) {
                        $('#status-verify-ecc').html("Unverified");
                    } else {
                        $('#status-verify-ecc').html("Verified");
                    }
                    $('#time-verify-ecc').html(new Date().getTime() - this.start_time +' ms');
                },
